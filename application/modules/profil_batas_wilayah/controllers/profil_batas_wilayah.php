    function save(){
        $data = $this->input->post();
        $this->load->library('form_validation');
        $this->form_validation->set_rules('id_penduduk','Nama','required');    
        $this->form_validation->set_rules('no_surat','Nomor surat','required');              
        $this->form_validation->set_message('required', ' %s Harus diisi '); 
        $this->form_validation->set_message('numeric', ' %s Harus diisi dengan angka ');
        $this->form_validation->set_message('alpha', ' %s Harus diisi dengan huruf ');
        
        
        $this->form_validation->set_error_delimiters("* ", '<br>');
        if($this->form_validation->run() == TRUE) {

                $data['id'] = md5(date("Ymdhis"));
                $data['tanggal'] = date("Y-m-d");

                $desa = $this->cm->data_desa();
                if($data['ttd'] == "kepala" ) {
                    $data['ttd'] = $data['ttd'];
                    $data['ttd_jabatan'] = "Kepala";
                    $data['ttd_nama'] = $desa->nama_kepala_desa;
                }
                else {
                    $data['ttd'] = $data['ttd'];
                    $data['ttd_jabatan'] = "Sekretaris ";
                    $data['ttd_nama'] = $desa->nama_sek_desa;
                }

                
                 
                $res = $this->db->insert("profile_batas_wilayah",$data);

                if($res) {    
                $ret = array("success"=>true,
                            "pesan"=>"Data berhasil disimpan",
                            "operation" =>"insert");
                }
                else {
                 $ret = array("success"=>false,
                            "pesan"=>"Data gagal disimpan " .mysql_error(),
                            "operation" =>"insert");   
                }

            }

        else {
             $ret = array("success"=>false,
                            "pesan"=>validation_errors(),
                            "operation" =>"insert");
        }
        echo json_encode($ret);
    }



